Welcome to the Soccer Schedule System.<br />
This is part of a project to create a framework for providing schedule and score updates for Soccer matches. It currently shows the schedule, score of the current season for teams and provides SMS updates for users in India (open only to restricted users. Leave me a comment if you want to join this system).<br /><br />

Currently we only support Manchester United (because no one else ever asked me to create a similar system for any other club :))<br /><br />

